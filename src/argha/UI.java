    private void printData() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        for (int i = 0; i < infoModels.size(); i++) {
            
            String place = infoModels.get(i).getPlace();
            String mag = infoModels.get(i).getMagnitutde();
            
            Object[] data = {place, mag};
            model.addRow(data);
        }
    }

    private void getData() {
        String data = new NetworkUtil().getData(url);
        infoModels = new ParseUtil().parseJson(data);
    }

